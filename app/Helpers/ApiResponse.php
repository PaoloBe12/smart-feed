<?php

namespace App\Helpers;

class ApiResponse
{
    public static function success($data = null, $message = "Successo!", $code = 200)
    {
        return response()->json([
            'status' => 'success',
            'message' => $message,
            'data' => $data
        ], $code);
    }

    public static function error($message = "Qualcosa Ã¨ andato storto!", $code = 400, $data = null)
    {
        return response()->json([
            'status' => 'error',
            'message' => $message,
            'data' => $data
        ], $code);
    }
}
